public class Car {
    // Attributes
    String make;
    String model;
    int year;
    String colour;

    // Methods
    public String getMake() {
        return this.make;
    }

    public void setMake(String newMake) {
        this.make = newMake;
    }

    public int getYear() {
        return this.year;
    }
    
    public void setYear(int newYear) {
        this.year = newYear;
    }
}